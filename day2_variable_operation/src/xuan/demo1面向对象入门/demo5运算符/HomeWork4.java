package xuan.demo1面向对象入门.demo5运算符;

public class HomeWork4 {
    public static void main(String[] args) {
        byte a = 10;
        System.out.println(a);
        a += 13.14; // a = (byte)(a + 13.14)
        System.out.println(a);

        a -= 5.2;  // a = (byte)(a - 5.2)
        System.out.println(a);
    }
}