package xuan.demo1面向对象入门.demo3数据类型;

public class HomeWork3 {
    public static void main(String[] args) {
        int a = 10;
        byte b = 100;
        byte c = 80;
        double d = 3.66;

        double e = a;
        short f = b;
        int g = c;
        int h = (int) d;

        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        System.out.println(h);
    }
}
